public class Swap {
    public static void main(String[] args) {
        int  a, b,temp;
        a=15;
        b=27;
        System.out.println("before swapping : a="+a+ "  b= " +b);
        temp=a;
        a=b;
        b=temp;
        System.out.println("After swapping : a="+a+ "  b= " +b);
//        System.out.println("after swapping : a,b="+a+ "," +b);
}
}

